// Define o pacote ao qual esta enumeração pertence
package Entities.Enums;

/**
 * A enumeração `WorkerLevel` representa os diferentes níveis de um trabalhador
 * em uma organização ou empresa. 
 * 
 * Esta enum é útil para categorizar os trabalhadores de acordo com sua experiência
 * ou posição hierárquica.
 */
public enum WorkerLevel {
    
    // Representa um trabalhador de nível Júnior (início de carreira)
    JUNIOR,

    // Representa um trabalhador de nível intermediário
    MID_LEVEL,

    // Representa um trabalhador de nível Sênior (mais experiente)
    SENIOR;
}